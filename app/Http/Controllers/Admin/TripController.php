    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $trip = Trip::find($id);
        $trip->delete();

        // redirect
        return redirect()->route('trips.index')
                        ->with('success','Trip deleted successfully');
    }
